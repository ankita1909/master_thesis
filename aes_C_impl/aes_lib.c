void keyExpansion(struct aes_data *self)
{
  uint32_t i, j, k;
  uint8_t tempa[4]; // Used for the column/row operations

  // The first round key is the key itself->
  for(i = 0; i < Nk; ++i)
  {
    self->RoundKey[(i * 4) + 0] = self->Key[(i * 4) + 0];
    self->RoundKey[(i * 4) + 1] = self->Key[(i * 4) + 1];
    self->RoundKey[(i * 4) + 2] = self->Key[(i * 4) + 2];
    self->RoundKey[(i * 4) + 3] = self->Key[(i * 4) + 3];
  }

  // All other round keys are found from the previous round keys.
  for(; (i < (Nb * (total_number_rounds + 1))); ++i)
  {
    for(j = 0; j < 4; ++j)
    {
      tempa[j]=self->RoundKey[(i-1) * 4 + j];
    }
    if (i % Nk == 0)
    {
      // This function rotates the 4 bytes in a word to the left once.
      // [a0,a1,a2,a3] becomes [a1,a2,a3,a0]

      // Function RotWord()
      {
        k = tempa[0];
        tempa[0] = tempa[1];
        tempa[1] = tempa[2];
        tempa[2] = tempa[3];
        tempa[3] = k;
      }

      // SubWord() is a function that takes a four-byte input word and
      // applies the S-box to each of the four bytes to produce an output word.

      // Function Subword()
      {
        tempa[0] = getSBoxValue(tempa[0]);
        tempa[1] = getSBoxValue(tempa[1]);
        tempa[2] = getSBoxValue(tempa[2]);
        tempa[3] = getSBoxValue(tempa[3]);
      }

      tempa[0] =  tempa[0] ^ Rcon[i/Nk];
    }
    else if (Nk > 6 && i % Nk == 4)
    {
      // Function Subword()
      {
        tempa[0] = getSBoxValue(tempa[0]);
        tempa[1] = getSBoxValue(tempa[1]);
        tempa[2] = getSBoxValue(tempa[2]);
        tempa[3] = getSBoxValue(tempa[3]);
      }
    }
    self->RoundKey[i * 4 + 0] = self->RoundKey[(i - Nk) * 4 + 0] ^ tempa[0];
    self->RoundKey[i * 4 + 1] = self->RoundKey[(i - Nk) * 4 + 1] ^ tempa[1];
    self->RoundKey[i * 4 + 2] = self->RoundKey[(i - Nk) * 4 + 2] ^ tempa[2];
    self->RoundKey[i * 4 + 3] = self->RoundKey[(i - Nk) * 4 + 3] ^ tempa[3];
  }

}
void addRoundKey(struct aes_data *self, uint8_t no_round)
{
  uint8_t i,j;
  for(i=0;i<16;++i)
  {
	  (self->state)[i] ^= self->RoundKey[no_round * Nb * 4 + i];
    }

}
